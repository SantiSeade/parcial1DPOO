package logica;

abstract public class Pieza {
	public int coordenadaX;
	
	public int coordenadaY;
	
	public int tamanioTablero;
	
	public int getCoordenadaX() {
		return coordenadaX;
	}
	
	public int getCoordenadaY() {
		return coordenadaY;
	}
	
	public int getTamanio(){
		return tamanioTablero;
	}
	
}

